package ebook.entity;

public class SeeUser {
    String logid;
    Integer nums;
    Float pays;

    public String getLogid() {
        return logid;
    }

    public void setLogid(String logid) {
        this.logid = logid;
    }

    public Integer getNums() {
        return nums;
    }

    public void setNums(Integer nums) {
        this.nums = nums;
    }

    public Float getPays() {
        return pays;
    }

    public void setPays(Float pays) {
        this.pays = pays;
    }
}